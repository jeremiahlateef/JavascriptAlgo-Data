// 1. Write a function that takes an array (a) and a value (n) as argument
// Return the nth element of 'a'
// How to solve: In order to get the nth element of a given array, you have to know its index. An index describes the position of an element in a given array. Javascript arrays are zero-indexed. This means that the first element of an array has index 0. The second element has index 1 and so on.
// Solution:
function myFunction(a, n) {
  return a[n];
}
